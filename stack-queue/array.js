// Part: Stack with push/pop
const stack = [];

stack.push(1);
stack.push(2);
stack.push(3);
stack.pop();
stack.pop();
stack.pop();

// Part: Stack with unshift/shift
stack.unshift(1);
stack.unshift(2);
stack.unshift(3);
stack.shift();
stack.shift();
stack.shift();

